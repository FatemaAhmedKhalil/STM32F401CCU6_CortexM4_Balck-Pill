#ifndef BITSOPERATION_H_
#define BITSOPERATION_H_

#define SET_BIT(x,BIT)    ( x|=(1<<BIT) )
#define CLR_BIT(x,BIT)    ( x&=(~(1<<BIT)) )
#define TOGGLE_BIT(x,BIT) ( x^=(1<<BIT) )
#define GET_BIT(x,BIT)    ( (x>>BIT)&1 )
#endif
