u8 PressedButton(SWITCH Switch)
{
	u8 SwitchState = 0;
	
	if (Switch.Type == PushButton)
		GPIO_GetPinValue (Switch.PORT, Switch.PIN, &SwitchState);
	
	return SwitchState;
}
