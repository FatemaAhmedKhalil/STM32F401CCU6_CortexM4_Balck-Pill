#include "Switch_Interface.h"

u8 Switch_u8PressedButton(SWITCH Switch)
{
	u8 u8SwitchState = 0;
	
	if (Switch.Type == PushButton)
		GPIO_u8GetPinValue (Switch.PORT, Switch.PIN, &u8SwitchState);
	
	return u8SwitchState;
}