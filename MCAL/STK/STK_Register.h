#ifndef STK_REGISTER_H_
#define STK_REGISTER_H_

#define STK_BaseAdress		(u32)(0xE000E010)

typedef struct
{
	u32	CTRL;		// STK control and status register
	u32	LOAD;		// STK reload value register
	u32	VAL;		// STK current value register
	u32	CALIB;		// STK calibration value register
}STK_MemoryMap;

#define	STK		( (volatile STK_MemoryMap *)(STK_BaseAdress) )


#endif