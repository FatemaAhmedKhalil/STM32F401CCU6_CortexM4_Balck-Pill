#ifndef SysTick_Register
#define SysTick_Register

#define SysTick_BaseAdress		(u32)(0xE000E010)

typedef struct
{
	u32	CTRL;		// SysTick control and status register
	u32	LOAD;		// SysTick reload value register
	u32	VAL;		// SysTick current value register
	u32	CALIB;		// SysTick calibration value register
}SysTick_MemoryMap;

#define	STK		( (volatile SysTick_MemoryMap *)(SysTick_BaseAdress) )


#endif